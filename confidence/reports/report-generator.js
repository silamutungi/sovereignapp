// confidence/reports/report-generator.js — Confidence report in 3 formats

export function generateJSON(report) {
  return JSON.stringify(report, null, 2)
}

export function generateMarkdown(report) {
  const band = report.band || 'UNKNOWN'
  const score = report.overall_score || 0
  const bar = buildProgressBar(score)

  const lines = [
    `# Sovereign Confidence Report`,
    `**${report.project_path || 'Project'} · ${new Date(report.evaluated_at || Date.now()).toDateString()}**`,
    '',
    `## Overall Score: ${score}/100 ${band}`,
    '',
    bar,
    '',
    `**Launch Gate:** ${report.launch_gate_passed ? '✅ PASSED' : '❌ FAILED'} (threshold: ${report.launch_gate_threshold || 75})`,
    '',
    '## Dimension Breakdown',
    '',
    '| Dimension | Score | Minimum | Status |',
    '|-----------|-------|---------|--------|',
  ]

  for (const [key, result] of Object.entries(report.dimensions || {})) {
    const dim = key.replace('_', ' ').replace(/\b\w/g, l => l.toUpperCase())
    const min = report.failed_minimums?.find(f => f.dimension === key)?.minimum || ''
    const status = min && result.score < min ? '❌ BELOW MIN' : '✅'
    lines.push(`| ${dim} | ${result.score}/100 | ${min} | ${status} |`)
  }

  if (report.critical_issues?.length > 0) {
    lines.push('', '## Critical Issues', '')
    for (const issue of report.critical_issues) {
      lines.push(`- **[${issue.severity}]** ${issue.message}`)
      if (issue.fix) lines.push(`  → Fix: ${issue.fix}`)
    }
  }

  if (report.recommendations?.length > 0) {
    lines.push('', '## Recommendations', '')
    for (const rec of report.recommendations) {
      lines.push(`- ${rec}`)
    }
  }

  lines.push('', '## What This Score Means', '')
  lines.push(interpretScore(score, report.launch_gate_passed))
  lines.push('', `---`, `*Generated by Sovereign Confidence Engine · ${new Date().toISOString()}*`)

  return lines.join('\n')
}

export function generateHTML(report) {
  const score = report.overall_score || 0
  const color = score >= 80 ? '#22c55e' : score >= 70 ? '#eab308' : '#ef4444'

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>Confidence Report — ${score}/100</title>
<style>
  body { font-family: 'DM Mono', monospace; background: #f2efe8; color: #0e0d0b; max-width: 800px; margin: 0 auto; padding: 2rem; }
  h1 { font-family: 'Playfair Display', serif; }
  .score { font-size: 4rem; font-weight: bold; color: ${color}; }
  .band { font-size: 1.25rem; color: #6b6862; }
  .bar { background: #e5e7eb; border-radius: 999px; height: 12px; margin: 1rem 0; }
  .bar-fill { background: ${color}; border-radius: 999px; height: 12px; width: ${score}%; }
  table { width: 100%; border-collapse: collapse; margin: 1rem 0; }
  th, td { padding: 0.75rem; border-bottom: 1px solid #e5e7eb; text-align: left; }
  th { background: #0e0d0b; color: #f2efe8; }
  .pass { color: #22c55e; }
  .fail { color: #ef4444; }
  .gate { padding: 1rem; border-radius: 8px; background: ${report.launch_gate_passed ? '#dcfce7' : '#fee2e2'}; border: 1px solid ${report.launch_gate_passed ? '#86efac' : '#fca5a5'}; margin: 1rem 0; }
</style>
</head>
<body>
<h1>Sovereign Confidence Report</h1>
<p class="band">${report.project_path || 'Project'} · ${new Date(report.evaluated_at || Date.now()).toDateString()}</p>
<div class="score">${score}/100 <span class="band">${report.band}</span></div>
<div class="bar"><div class="bar-fill"></div></div>
<div class="gate">${report.launch_gate_passed ? '✅ Launch gate PASSED' : '❌ Launch gate FAILED'} — threshold: ${report.launch_gate_threshold || 75}/100</div>
<h2>Dimension Breakdown</h2>
<table>
<tr><th>Dimension</th><th>Score</th><th>Minimum</th><th>Status</th></tr>
${Object.entries(report.dimensions || {}).map(([key, result]) => {
  const min = report.failed_minimums?.find(f => f.dimension === key)?.minimum || ''
  const pass = !min || result.score >= min
  return `<tr><td>${key}</td><td>${result.score}/100</td><td>${min}</td><td class="${pass ? 'pass' : 'fail'}">${pass ? '✅' : '❌'}</td></tr>`
}).join('\n')}
</table>
</body>
</html>`
}

function buildProgressBar(score) {
  const filled = Math.round(score / 5)
  const empty = 20 - filled
  return `[${'█'.repeat(filled)}${'░'.repeat(empty)}] ${score}/100`
}

function interpretScore(score, launched) {
  if (score >= 90) return 'Exceptional quality. This app is production-ready and exceeds the bar we set for customers.'
  if (score >= 80) return 'Strong quality. Launch gate passed. Continue improving the lower-scoring dimensions.'
  if (score >= 75) return 'Good enough to launch. Address the critical issues before going to market.'
  if (score >= 60) return 'Adequate for internal testing. Fix the critical and high issues before user-facing launch.'
  return 'Critical issues present. Do not launch until security and code quality minimums are met.'
}
