// company-os/coach/coach-weekly-brief.js
// Generates the weekly intelligence brief for each active build.

import { writeFileSync, mkdirSync, existsSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'
import { loadContext } from '../handoff/context-transfer.js'
import * as CoachMemory from './coach-memory.js'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)
const BRIEFS_DIR = join(__dirname, '..', 'briefs')

export async function generateWeeklyBrief(buildId) {
  const context = loadContext(buildId)
  if (!context) return null

  const memory = CoachMemory.loadMemory(buildId)
  const metrics = context.metrics || {}
  const weekAgo = new Date(Date.now() - 7 * 24 * 60 * 60 * 1000)

  const brief = {
    build_id: buildId,
    app_name: context.app_name,
    generated_at: new Date().toISOString(),
    period: { from: weekAgo.toISOString(), to: new Date().toISOString() },
    sections: {
      key_metrics: buildKeyMetrics(metrics, context),
      wins: buildWins(context, metrics),
      focus: buildFocus(context, memory),
      intelligence: buildIntelligence(context),
      next_steps: buildNextSteps(context, metrics),
    },
  }

  // Save brief
  if (!existsSync(BRIEFS_DIR)) mkdirSync(BRIEFS_DIR, { recursive: true })
  const date = new Date().toISOString().split('T')[0]
  const briefPath = join(BRIEFS_DIR, `${buildId}-${date}.json`)
  writeFileSync(briefPath, JSON.stringify(brief, null, 2))

  const mdPath = join(BRIEFS_DIR, `${buildId}-${date}.md`)
  writeFileSync(mdPath, formatBriefMarkdown(brief))

  return brief
}

export function formatBriefMarkdown(brief) {
  const s = brief.sections
  return `# Weekly Intelligence Brief — ${brief.app_name}
*${new Date(brief.generated_at).toDateString()}*

---

## 📊 Key Metrics
${s.key_metrics.map(m => `- **${m.label}:** ${m.value} ${m.trend || ''}`).join('\n')}

## 🎉 Wins
${s.wins.map(w => `- ${w}`).join('\n')}

## 🎯 Focus This Week
**${s.focus.title}**
${s.focus.description}

## 💡 Intelligence
${s.intelligence}

## ✅ Next Steps
${s.next_steps.map((step, i) => `${i + 1}. ${step.action} *(${step.effort})*`).join('\n')}

---
*Generated by Sovereign Coach*`
}

export function formatBriefJSON(brief) {
  return JSON.stringify(brief, null, 2)
}

export function scheduleBrief(buildId) {
  // Record scheduling intent — actual cron handled by Vercel cron config
  console.log(`[Coach Weekly Brief] Scheduling weekly brief for build: ${buildId}`)
  return { scheduled: true, build_id: buildId, frequency: 'weekly', next_run: getNextMonday() }
}

function buildKeyMetrics(metrics, context) {
  const kpis = [
    { label: 'Users', value: metrics.users || 0, benchmark: 100, trend: null },
    { label: 'Revenue', value: metrics.revenue ? `$${metrics.revenue}` : '$0', benchmark: null, trend: null },
    { label: 'Confidence Score', value: `${context.confidence_score || 'N/A'}/100`, benchmark: 75, trend: null },
    { label: 'Deploys', value: metrics.deploys || 1, benchmark: null, trend: null },
  ]
  return kpis.map(k => ({
    ...k,
    trend: k.benchmark && k.value < k.benchmark ? '↓ below benchmark' : k.benchmark ? '✓ on track' : null,
  }))
}

function buildWins(context, metrics) {
  const wins = []
  if (context.deployed_at) wins.push(`${context.app_name} is live at ${context.deploy_url || 'your domain'}`)
  if ((metrics.users || 0) > 0) wins.push(`${metrics.users} users have signed up`)
  if (context.confidence_score >= 75) wins.push(`Confidence score of ${context.confidence_score}/100 — launch gate passed`)
  if (wins.length === 0) wins.push('Your app was deployed successfully this week')
  return wins
}

function buildFocus(context, memory) {
  const focusArea = memory?.focus_area || 'growth'
  const focuses = {
    growth: { title: 'Acquire your first 10 users', description: 'Personal outreach converts at 30-50%. Email 20 people. Expect 6-10 signups. This week only.' },
    product: { title: 'Fix the top user friction point', description: 'Check your analytics for where users drop off. Fix the top one thing. Ship it before Friday.' },
    technical: { title: 'Improve your confidence score', description: 'Review the confidence report. The highest-impact issue is usually under 2 hours of work.' },
    business: { title: 'Define your pricing', description: 'Free apps don\'t validate willingness to pay. Add a paid tier — even a simple $9/month option — and see who pays.' },
  }
  return focuses[focusArea] || focuses.growth
}

function buildIntelligence(context) {
  if (context.confidence_score < 75) {
    return `Your confidence score of ${context.confidence_score}/100 means your app has specific areas below the quality bar. The most common blockers at this score: missing loading states, accessibility issues, or incomplete error handling.`
  }
  return `Your app is performing well technically. The next leverage point is growth: ${(context.metrics?.users || 0) === 0 ? 'getting your first user is the most important thing you can do this week' : 'doubling your user count in the next 30 days'}.`
}

function buildNextSteps(context, metrics) {
  const steps = []
  if ((metrics.users || 0) === 0) {
    steps.push({ action: 'Share app with 10 people personally', effort: '30 min', priority: 1 })
  }
  if (context.confidence_score < 80) {
    steps.push({ action: 'Review and fix top confidence issue', effort: '1-2 hours', priority: 2 })
  }
  steps.push({ action: 'Ship one improvement based on any feedback', effort: '2-4 hours', priority: 3 })
  return steps.slice(0, 3)
}

function getNextMonday() {
  const d = new Date()
  const day = d.getDay()
  const daysUntilMonday = day === 0 ? 1 : 8 - day
  d.setDate(d.getDate() + daysUntilMonday)
  d.setHours(0, 0, 0, 0)
  return d.toISOString()
}
