// agents/build/mobile-pwa-agent.js
// Takes frontend files. Adds PWA manifest and service worker.
// Returns: { manifest: object, service_worker: string }

import { AgentBase } from '../../shared/agent-base-class.js'

class MobilePwaAgent extends AgentBase {
  constructor() {
    super({ name: 'mobile-pwa-agent', phase: 'build', version: '1.0.0' })
  }

  async run(context) {
    const { spec, generatedFiles } = context

    if (!spec) {
      throw new Error('mobile-pwa-agent requires spec in context')
    }

    const appName = spec.name || 'My App'
    const primaryColor = spec.primaryColor || '#6366f1'
    const backgroundColor = '#f2efe8'

    this.log('info', 'Generating PWA manifest and service worker', { app_name: appName })

    const manifest = this._buildManifest(appName, primaryColor, backgroundColor, spec)
    const service_worker = this._buildServiceWorker(appName, spec)

    // Patch index.html to include manifest link and theme-color meta
    const patched_files = { ...(generatedFiles || {}) }
    if (patched_files['index.html']) {
      patched_files['index.html'] = this._patchIndexHtml(patched_files['index.html'], primaryColor)
    }

    // Add manifest and SW files
    patched_files['public/manifest.json'] = JSON.stringify(manifest, null, 2)
    patched_files['public/sw.js'] = service_worker

    this.log('info', 'PWA assets generated', {
      manifest_icons: manifest.icons.length,
      sw_cached_routes: manifest.start_url ? 1 : 0,
    })

    return { manifest, service_worker, patched_files }
  }

  _buildManifest(appName, primaryColor, backgroundColor, spec) {
    const shortName = appName.length > 12 ? appName.slice(0, 12) : appName
    const description = spec?.description || `${appName} — web app`

    return {
      name: appName,
      short_name: shortName,
      description,
      start_url: '/',
      scope: '/',
      display: 'standalone',
      orientation: 'portrait-primary',
      theme_color: primaryColor,
      background_color: backgroundColor,
      lang: 'en',
      categories: ['productivity', 'utilities'],
      icons: [
        {
          src: '/icon-192.png',
          sizes: '192x192',
          type: 'image/png',
          purpose: 'any maskable',
        },
        {
          src: '/icon-512.png',
          sizes: '512x512',
          type: 'image/png',
          purpose: 'any maskable',
        },
      ],
      screenshots: [],
      shortcuts: [],
    }
  }

  _buildServiceWorker(appName, spec) {
    const cacheName = `${(appName || 'app').toLowerCase().replace(/\s+/g, '-')}-v1`

    // Routes to pre-cache on install
    const staticRoutes = ['/', '/index.html']
    if (spec?.tier === 'STANDARD' || spec?.tier === 'COMPLEX') {
      staticRoutes.push('/login', '/signup', '/dashboard')
    }

    const cachedRoutesJson = JSON.stringify(staticRoutes, null, 2)

    return `// Service Worker for ${appName}
// Generated by Sovereign mobile-pwa-agent
// Strategy: Cache-first for static assets, Network-first for API routes

const CACHE_NAME = '${cacheName}'
const STATIC_ROUTES = ${cachedRoutesJson}

// Install — pre-cache static routes
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(STATIC_ROUTES).catch((err) => {
        console.warn('[SW] Pre-cache failed for some routes:', err)
      })
    })
  )
  self.skipWaiting()
})

// Activate — clean up old caches
self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(
        keys
          .filter((key) => key !== CACHE_NAME)
          .map((key) => {
            console.log('[SW] Deleting old cache:', key)
            return caches.delete(key)
          })
      )
    )
  )
  self.clients.claim()
})

// Fetch — Network-first for API routes, Cache-first for everything else
self.addEventListener('fetch', (event) => {
  const url = new URL(event.request.url)

  // Never intercept API calls — always go to network
  if (url.pathname.startsWith('/api/')) {
    return
  }

  // For GET requests: try cache first, fall back to network
  if (event.request.method !== 'GET') return

  event.respondWith(
    caches.match(event.request).then((cached) => {
      if (cached) return cached

      return fetch(event.request)
        .then((response) => {
          // Only cache successful same-origin responses
          if (
            response.ok &&
            response.type === 'basic' &&
            url.origin === self.location.origin
          ) {
            const clone = response.clone()
            caches.open(CACHE_NAME).then((cache) => cache.put(event.request, clone))
          }
          return response
        })
        .catch(() => {
          // Offline fallback for navigation requests
          if (event.request.mode === 'navigate') {
            return caches.match('/index.html')
          }
          return new Response('Offline', { status: 503 })
        })
    })
  )
})
`
  }

  _patchIndexHtml(html, primaryColor) {
    let patched = html

    // Add theme-color meta if missing
    if (!patched.includes('theme-color')) {
      patched = patched.replace(
        '</head>',
        `  <meta name="theme-color" content="${primaryColor}" />\n  <link rel="manifest" href="/manifest.json" />\n</head>`
      )
    }

    // Register service worker before </body>
    if (!patched.includes('serviceWorker')) {
      patched = patched.replace(
        '</body>',
        `  <script>
    if ('serviceWorker' in navigator) {
      window.addEventListener('load', () => {
        navigator.serviceWorker.register('/sw.js').catch(() => {})
      })
    }
  </script>
</body>`
      )
    }

    return patched
  }

  async verify(output) {
    const { manifest } = output

    const requiredFields = ['name', 'short_name', 'start_url', 'display', 'icons', 'theme_color']
    for (const field of requiredFields) {
      if (!manifest[field]) {
        this.logIssue({
          severity: 'medium',
          message: `PWA manifest missing required field: ${field}`,
          file: 'manifest.json',
        })
      }
    }

    if (!manifest.icons || manifest.icons.length < 2) {
      this.logIssue({
        severity: 'medium',
        message: 'PWA manifest should include at least 192x192 and 512x512 icons',
        file: 'manifest.json',
      })
    }

    return output
  }
}

export default async function run(context) {
  return new MobilePwaAgent().execute(context)
}
