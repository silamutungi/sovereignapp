// agents/build/analytics-agent.js
// Takes product spec. Produces event tracking plan.
// Events: page_view, signup, login, key_action (product-specific), conversion.
// Returns: { events: Event[], tracking_code: string }

import { AgentBase } from '../../shared/agent-base-class.js'

// Standard events every app must track
const STANDARD_EVENTS = [
  {
    name: 'page_view',
    trigger: 'Every route change',
    properties: ['page', 'referrer', 'user_id'],
    category: 'navigation',
  },
  {
    name: 'signup',
    trigger: 'User completes registration',
    properties: ['method', 'plan', 'referrer'],
    category: 'auth',
  },
  {
    name: 'login',
    trigger: 'User authenticates successfully',
    properties: ['method', 'user_id'],
    category: 'auth',
  },
  {
    name: 'conversion',
    trigger: 'User completes primary value action',
    properties: ['action', 'value', 'currency', 'user_id'],
    category: 'business',
  },
]

class AnalyticsAgent extends AgentBase {
  constructor() {
    super({ name: 'analytics-agent', phase: 'build', version: '1.0.0' })
  }

  async run(context) {
    const { spec } = context

    if (!spec) {
      throw new Error('analytics-agent requires spec in context')
    }

    this.log('info', 'Building event tracking plan', { app_name: spec.name })

    const events = [...STANDARD_EVENTS]

    // Derive key_action events from spec features
    const features = spec.features || []
    for (const feature of features) {
      const eventName = this._featureToEventName(feature)
      events.push({
        name: eventName,
        trigger: `User performs: ${feature}`,
        properties: ['user_id', 'item_id', 'timestamp'],
        category: 'key_action',
      })
    }

    // Add entity-specific events
    const entities = spec.entities || []
    for (const entity of entities) {
      const slug = entity.toLowerCase().replace(/\s+/g, '_')
      events.push({
        name: `${slug}_created`,
        trigger: `User creates a ${entity}`,
        properties: ['user_id', `${slug}_id`, 'timestamp'],
        category: 'key_action',
      })
    }

    const tracking_code = this._generateTrackingCode(events, spec)

    this.log('info', 'Analytics plan complete', { total_events: events.length })

    return { events, tracking_code }
  }

  _featureToEventName(feature) {
    return feature
      .toLowerCase()
      .replace(/[^a-z0-9\s]/g, '')
      .trim()
      .replace(/\s+/g, '_')
  }

  _generateTrackingCode(events, spec) {
    const appName = spec.name || 'app'
    const eventNames = events.map(e => e.name)

    return `// analytics.ts — Event tracking for ${appName}
// Generated by Sovereign analytics-agent
// All analytics calls are fire-and-forget — never block the UI

type EventName = ${eventNames.map(n => `'${n}'`).join(' | ')}

interface TrackOptions {
  [key: string]: string | number | boolean | null | undefined
}

function track(event: EventName, properties: TrackOptions = {}): void {
  try {
    // Replace this with your analytics provider (PostHog, Mixpanel, Segment, etc.)
    // Example: posthog.capture(event, { ...properties, app: '${appName}' })
    if (typeof window !== 'undefined' && (window as any).analytics) {
      ;(window as any).analytics.track(event, {
        ...properties,
        app: '${appName}',
        timestamp: new Date().toISOString(),
      })
    }
    // Always log to console in development
    if (import.meta.env.DEV) {
      console.log('[analytics]', event, properties)
    }
  } catch {
    // Never throw — analytics must never break the UI
  }
}

// Page view — call on every route change
export function trackPageView(page: string, referrer?: string): void {
  track('page_view', { page, referrer: referrer ?? document.referrer })
}

// Auth events
export function trackSignup(method: string, plan?: string): void {
  track('signup', { method, plan: plan ?? 'free' })
}

export function trackLogin(method: string, userId?: string): void {
  track('login', { method, user_id: userId ?? 'anonymous' })
}

// Conversion — call when user completes the primary value action
export function trackConversion(action: string, value?: number): void {
  track('conversion', { action, value: value ?? 0 })
}

${events
  .filter(e => e.category === 'key_action')
  .map(e => {
    const fnName = 'track' + e.name.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('')
    return `// ${e.trigger}
export function ${fnName}(userId?: string, itemId?: string): void {
  track('${e.name}', { user_id: userId, item_id: itemId })
}`
  })
  .join('\n\n')}

export { track }
export type { EventName }
`
  }

  async verify(output) {
    const required = ['page_view', 'signup', 'login', 'conversion']
    const eventNames = output.events.map(e => e.name)

    for (const req of required) {
      if (!eventNames.includes(req)) {
        this.logIssue({
          severity: 'high',
          message: `Required event "${req}" missing from tracking plan`,
          file: 'analytics-agent',
        })
      }
    }

    return output
  }
}

export default async function run(context) {
  return new AnalyticsAgent().execute(context)
}
