// agents/vision/design-tokens-agent.js
// Generates tokens.css from design direction. Applies WCAG brightness formula.
// Returns { css: string, tokens: object } — the css is ready to write to src/styles/tokens.css.
import { AgentBase } from '../../shared/agent-base-class.js'

export class DesignTokensAgent extends AgentBase {
  constructor() {
    super({ name: 'design-tokens-agent', phase: 'vision', version: '1.0.0' })
  }

  async run(context) {
    const { designDirection } = context
    this.log('info', 'Generating design tokens CSS')

    const palette = designDirection?.palette || this.defaultPalette()
    const typography = designDirection?.typography || this.defaultTypography()
    const spacing = designDirection?.spacing || [4, 8, 16, 24, 32, 48, 64]
    const motion = designDirection?.motion || { entrance: '0.4s ease', transition: '0.15s ease', delight: '0.6s ease' }

    // Enforce WCAG AA — brightness formula from CLAUDE.md
    const verifiedPalette = this.enforceContrast(palette)
    const tokens = this.buildTokens(verifiedPalette, typography, spacing, motion, designDirection)
    const css = this.renderCSS(tokens)

    return { css, tokens }
  }

  defaultPalette() {
    return {
      primary: '#c8f060',
      background: '#f2efe8',
      text: '#0e0d0b',
      surface: '#ffffff',
      muted: '#6b6862',
    }
  }

  defaultTypography() {
    return { heading: 'Playfair Display', body: 'DM Mono', scale: [12, 14, 16, 18, 24, 32, 48] }
  }

  // brightness = (R×299 + G×587 + B×114) / 1000
  // > 128 = LIGHT → use #1a1a1a text
  // ≤ 128 = DARK → use #ffffff text
  calculateBrightness(hex) {
    const r = parseInt(hex.slice(1, 3), 16)
    const g = parseInt(hex.slice(3, 5), 16)
    const b = parseInt(hex.slice(5, 7), 16)
    return (r * 299 + g * 587 + b * 114) / 1000
  }

  darkenColor(hex, factor = 0.65) {
    const r = Math.round(parseInt(hex.slice(1, 3), 16) * factor)
    const g = Math.round(parseInt(hex.slice(3, 5), 16) * factor)
    const b = Math.round(parseInt(hex.slice(5, 7), 16) * factor)
    return `#${r.toString(16).padStart(2, '0')}${g.toString(16).padStart(2, '0')}${b.toString(16).padStart(2, '0')}`
  }

  enforceContrast(palette) {
    const brightness = this.calculateBrightness(palette.primary)
    return {
      ...palette,
      text_on_primary: brightness > 128 ? '#1a1a1a' : '#ffffff',
      // Outline buttons darken primary by ×0.65 per CLAUDE.md rule
      primary_dark: this.darkenColor(palette.primary, 0.65),
      // Dark section secondary text — never use muted on dark backgrounds
      text_on_dark: '#c8c4bc',
      // Paper section secondary text
      text_dim: '#6b6862',
    }
  }

  buildTokens(palette, typography, spacing, motion, designDirection) {
    const borderRadius = designDirection?.border_radius || 6
    return {
      color: {
        primary: palette.primary,
        'primary-dark': palette.primary_dark,
        'text-on-primary': palette.text_on_primary,
        background: palette.background,
        surface: palette.surface,
        text: palette.text,
        'text-dim': palette.text_dim,
        'text-on-dark': palette.text_on_dark,
        muted: palette.muted,
        success: '#22c55e',
        error: '#ef4444',
        warning: '#f59e0b',
        info: '#3b82f6',
      },
      font: {
        heading: `'${typography.heading}', Georgia, serif`,
        body: `'${typography.body}', 'Courier New', monospace`,
        'size-xs': `${typography.scale[0]}px`,
        'size-sm': `${typography.scale[1]}px`,
        'size-base': `${typography.scale[2]}px`,
        'size-md': `${typography.scale[3]}px`,
        'size-lg': `${typography.scale[4]}px`,
        'size-xl': `${typography.scale[5]}px`,
        'size-2xl': `${typography.scale[6]}px`,
      },
      space: spacing.reduce((acc, val, i) => {
        acc[i + 1] = `${val}px`
        return acc
      }, {}),
      radius: {
        sm: `${borderRadius}px`,
        md: `${borderRadius * 2}px`,
        lg: `${borderRadius * 3}px`,
        full: '9999px',
      },
      motion: {
        entrance: motion.entrance,
        transition: motion.transition,
        delight: motion.delight,
      },
    }
  }

  renderCSS(tokens) {
    const lines = [
      '/* Sovereign Design Tokens — generated by design-tokens-agent */',
      '/* Do not edit manually — regenerate via Sovereign */',
      ':root {',
      '',
      '  /* Colors */',
    ]

    for (const [name, value] of Object.entries(tokens.color)) {
      lines.push(`  --color-${name}: ${value};`)
    }

    lines.push('', '  /* Typography */')
    for (const [name, value] of Object.entries(tokens.font)) {
      lines.push(`  --font-${name}: ${value};`)
    }

    lines.push('', '  /* Spacing */')
    for (const [name, value] of Object.entries(tokens.space)) {
      lines.push(`  --space-${name}: ${value};`)
    }

    lines.push('', '  /* Border Radius */')
    for (const [name, value] of Object.entries(tokens.radius)) {
      lines.push(`  --radius-${name}: ${value};`)
    }

    lines.push('', '  /* Motion */')
    for (const [name, value] of Object.entries(tokens.motion)) {
      lines.push(`  --motion-${name}: ${value};`)
    }

    lines.push('}', '')
    return lines.join('\n')
  }
}

export default async function run(context) {
  return new DesignTokensAgent().execute(context)
}
